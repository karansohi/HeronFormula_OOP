package triangle;
public class Point {
    //declaration of the coordinates of the point on the x y axis
    private double x;
    private double y;
    //constructor
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }
    //getters and setters
    public double getX() {
        return x;
    }
    public void setX(double x) {
        this.x = x;
    }
    public double getY() {
        return y;
    }
    public void setY(double y) {
        this.y = y;
    }

}

